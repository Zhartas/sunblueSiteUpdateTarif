$('#contact').click(function() {
  var items = [];
  $( $(this).attr('href') ).find('.slide-pop_up-contact').each(function() { items.push( { src: $(this)  } ); });
  $.magnificPopup.open({ items:items, gallery: { enabled: true } }); 
});

